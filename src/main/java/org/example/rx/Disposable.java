package org.example.rx;

/**
 * Интерфейс, представляющий ресурс, который можно освободить (отписаться),
 * чтобы отменить подписку или освободить связанные ресурсы.
 */
public interface Disposable {

    /**
     * Освобождает ресурс. Метод должен быть идемпотентным, то есть повторный вызов
     * не должен изменять состояние после первого вызова.
     */
    void dispose();

    /**
     * Проверяет, был ли ресурс уже освобожден.
     * @return true, если ресурс был освобожден; иначе false
     */
    boolean isDisposed();
}
